export const generate24HourData = (baseValue: number, variance: number) => {
  const data = [];
  for (let i = 0; i < 24; i++) {
    const hourFactor = i >= 8 && i <= 20 ? 1.2 : 0.6;
    const value = baseValue * hourFactor + (Math.random() - 0.5) * variance;
    data.push({
      hour: `${i.toString().padStart(2, '0')}:00`,
      value: Math.round(Math.max(0, value) * 10) / 10,
    });
  }
  return data;
};
